
import math

def suma (n1,n2):
    return(n1+n2)
print(suma(3,5))

def resta (n1,n2):
    return(n1-n2)
print(resta(10,1))

def mult (n1,n2):
    return(n1*n2)
print(mult(4,3))

def divi (n1,n2):
    return(n1/n2)
print(divi(10,5))

def pot (n1,n2):
    return(n1**n2)
print(pot(5,3))

def coseno (n1):
    return(math.cos(n1))
print(coseno(5))


def seno (n1):
    return(math.sin(n1))
print(seno(7))